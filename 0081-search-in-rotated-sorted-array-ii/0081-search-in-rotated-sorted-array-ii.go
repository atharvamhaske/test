func search(nums []int, target int) bool {
    n := len(nums)
    l, r := 0, n-1

    for l <= r {
        mid := l + (r-l)/2
        if nums[mid] == target {
            return true
        } else if nums[l] == nums[mid] && nums[mid] == nums[r] {
            l += 1
            r -= 1
        } else if nums[l] <= nums[mid] {
            if nums[l] <= target && target < nums[mid] {
                r = mid - 1
            } else {
                l = mid + 1
            }
        } else {
            if nums[mid] < target && target <= nums[r] {
                l = mid + 1
            } else {
                r =  mid - 1
            }
        }
    }
    return false
}